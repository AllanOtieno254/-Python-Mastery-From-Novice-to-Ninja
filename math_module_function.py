import math
pi=3.14
print(round(pi))
#ceil rounding up to nearest integer no matter if the value behind is above 5 or not
print(math.ceil(pi))
#floor for rounding number down
print(math.floor(pi))
#absolute(abs): gives the absolute value for a value. tells you how far a number is far away from zero
pi_1=-3.14
print(abs(pi_1))
#pow : raise base number
print(pow(pi,2))
#squareroot
print(math.sqrt(16))
#max function
x,y,z=3,29,7
print(max(x,y,z))
#min function
print(min(x,y,z))