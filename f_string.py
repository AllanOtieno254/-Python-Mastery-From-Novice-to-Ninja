# What is an f-string in Python?
# An f-string (short for formatted string) is a way to insert variables directly into a string using {}.
#
# It starts with the letter f before the string, like this:

name = "Alice"
print(f"Hello, {name}!")  # ➜ Hello, Alice!

name = "Peter"
age = 20
print(f"{name} is {age} years old")  # ➜ Peter is 20 years old